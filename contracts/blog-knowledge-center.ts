function categoryLink(slug: string, label: string, note: string): KnowledgeLink {
  return {
    label,
    href: `/catalog?cat=${encodeURIComponent(slug)}`,
    note,
  };
}

function brandLink(slug: string, label: string, note: string): KnowledgeLink {
  return {
    label,
    href: `/catalog?view=brands&brand=${encodeURIComponent(slug)}`,
    note,
  };
}

