export function getKnowledgeCenterPlanForPost(input: {
  slug?: string | null;
  title?: string | null;
  category?: string | null;
}) {
  const slug = (input.slug || "").trim().toLowerCase();
  const title = (input.title || "").trim().toLowerCase();

  const exact = knowledgeCenterArticlePlan.find(item => item.slug === slug);
  if (exact) return exact;

  const partial = knowledgeCenterArticlePlan.find(item => {
    const normalizedTitle = item.title.toLowerCase();
    return title.length > 0 && (title.includes(normalizedTitle) || normalizedTitle.includes(title));
  });

  if (partial) return partial;

  return null;
}

